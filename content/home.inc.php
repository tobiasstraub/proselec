<b>Was ist ProSelec?</b><br />
<br />
Im Rahmen des Informatik Workshops der Fakultät Informatik an der Hochschule Furtwangen University, wurde ein System erschaffen, welches die Vergabe von Projekten an Studenten in allen HFU Fakultäten erleichtert. Hierbei handelt es sich um eine Webseite, die in aller erster Linie, die von Professoren und Dozenten erstellten Projekte, sinnvoll an interessierte Studenten vergibt. Studenten können dabei je ein Primär- und ein Sekundär Projekt wählen. Darüber hinaus erhält man auf der Plattform, eine Übersicht aller zur verfügungstehenden Projekten, mit übersichtlich angezeigten Informationen wie Gruppenmitglieder, Gruppensprecher und Projektphase.
